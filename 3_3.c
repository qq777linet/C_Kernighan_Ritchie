#include <stdio.h>
#include <ctype.h>

/* Функция expand: разворачивает сокращенные записи (например, a-z) в полный диапазон (abc...xyz) */
void expand(const char s1[], char s2[]) 
{
    int i = 0; // Индекс для строки s1
    int j = 0; // Индекс для строки s2

    while (s1[i] != '\0') 
    {
        if (s1[i] != '-' || i == 0 || s1[i + 1] == '\0') 
        {
            // Если текущий символ не '-', или '-' в начале/конце строки
            s2[j++] = s1[i++];
        } else if (isalnum(s1[i - 1]) && isalnum(s1[i + 1]) && s1[i - 1] < s1[i + 1]) 
        {
            // Если это допустимый диапазон (a-z, 0-9 и т.д.)
            char start = s1[i - 1];
            char end = s1[i + 1];
            for (char c = start + 1; c < end; c++) 
            {
                s2[j++] = c; // Добавляем символы диапазона
            }
            i += 2; // Пропускаем '-' и конец диапазона
        } else 
        {
            // В других случаях просто копируем символ '-'
            s2[j++] = s1[i++];
        }
    }

    s2[j] = '\0'; // Завершаем строку
}
